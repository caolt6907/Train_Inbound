package com.strategy.vo;

public class Ai {
	private String aiId;
	private String lyCode;
	private String type;
	private String content;
	private String evaluation;
	private String nextAIid;
	private String aicontent;
	public String getAiId() {
		return aiId;
	}
	public void setAiId(String aiId) {
		this.aiId = aiId;
	}
	public String getLyCode() {
		return lyCode;
	}
	public void setLyCode(String lyCode) {
		this.lyCode = lyCode;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getEvaluation() {
		return evaluation;
	}
	public void setEvaluation(String evaluation) {
		this.evaluation = evaluation;
	}
	public String getNextAIid() {
		return nextAIid;
	}
	public void setNextAIid(String nextAIid) {
		this.nextAIid = nextAIid;
	}
	public String getAicontent() {
		return aicontent;
	}
	public void setAicontent(String aicontent) {
		this.aicontent = aicontent;
	}

}
